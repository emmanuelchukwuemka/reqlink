/**
 * ResQLink PWA + Push Notification Registration
 */
(function() {
    // Register service worker
    if ('serviceWorker' in navigator) {
        navigator.serviceWorker.register('/sw.js', { scope: '/' })
            .then(reg => {
                console.log('[ResQLink] SW registered:', reg.scope);
                // Ask for push permission after SW ready
                if ('Notification' in window && 'PushManager' in window) {
                    requestPushPermission(reg);
                }
            })
            .catch(err => console.warn('[ResQLink] SW registration failed:', err));
    }

    function requestPushPermission(reg) {
        if (Notification.permission === 'granted') {
            subscribePush(reg);
        } else if (Notification.permission !== 'denied') {
            Notification.requestPermission().then(perm => {
                if (perm === 'granted') subscribePush(reg);
            });
        }
    }

    function subscribePush(reg) {
        // We use FCM token approach (Legacy HTTP API) — no VAPID needed client-side
        // Just store a flag that this device wants push, and let the server send via FCM
        // For browser push, we'd need VAPID. For simplicity, we use the FCM approach
        // where the token is generated by the Firebase JS SDK on the client.
        // Since we're not loading Firebase JS SDK here, we'll use a minimal approach:
        // store in localStorage that push is enabled, server uses stored FCM tokens.

        // Try to get existing push subscription for unsubscribe support
        reg.pushManager.getSubscription().then(sub => {
            if (!sub) {
                // No subscription yet; send an empty registration to server to note interest
                // Real FCM token would come from firebase.messaging().getToken()
                fetch('/push/subscribe', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': (document.querySelector('meta[name="csrf-token"]') || {}).content || ''
                    },
                    body: JSON.stringify({ endpoint: 'browser', platform: navigator.userAgent.substring(0, 60) })
                }).catch(() => {});
            }
        }).catch(() => {});
    }
})();
